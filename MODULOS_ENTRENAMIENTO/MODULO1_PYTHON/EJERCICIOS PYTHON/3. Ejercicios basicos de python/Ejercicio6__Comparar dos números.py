Numero1 = float(input("\nIngresa el primer número: "))
Numero2 = float(input("\nIngresa el segundo número: "))
if Numero1 > Numero2:
    print(f"\nEl número {Numero1} es mayor que {Numero2}.\n")
elif Numero1 < Numero2:
    print(f"\nEl número {Numero2} es mayor que {Numero1}.\n")
else:
    print(f"\nAmbos números son iguales.\n")            